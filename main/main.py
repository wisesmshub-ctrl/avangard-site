import streamlit as st

# Настройка вкладки браузера
st.set_page_config(page_title="Республика Авангард", page_icon="🛡️", layout="centered")

# Главный заголовок сайта
st.title("🛡️ Официальный сайт Республики Авангард")

# Текст приветствия
st.write("Добро пожаловать в Авангард — прогрессивное виртуальное государство, основанное на чести и развитии!")

# Показываем твой новый флаг
# Картинка загружается по прямой ссылке
flag_url = "https://googleusercontent.com"
st.image(flag_url, caption="Государственный флаг Республики Авангард", use_container_width=True)

# Кнопка для скачивания флага гражданами
st.download_button(
    label="💾 Скачать флаг Авангарда",
    data=flag_url,
    file_name="avangard_flag.jpg",
    mime="image/jpeg"
)

st.write("---")

# Раздел с профилями и кнопками
st.header("🪪 Паспортный стол")
st.write("Нажми на кнопку гражданина, чтобы открыть его личный профиль и паспорт:")

# Создаем две колонки для кнопок профилей
col1, col2 = st.columns(2)

with col1:
    if st.button("👤 Профиль: Президент"):
        st.success("🔓 Доступ разрешен: Личный кабинет Правителя")
        st.markdown("""
        * **Имя:** Создатель Авангарда
        * **Должность:** Президент / Основатель
        * **ID-Паспорт:** № 000001
        * **Статус:** В сети
        """)

with col2:
    if st.button("👤 Профиль: Никита"):
        st.info("🔓 Доступ разрешен: Личный кабинет Гражданина")
        st.markdown("""
        * **Имя:** Никита
        * **Должность:** Министр Информации
        * **ID-Паспорт:** № 000002
        * **Статус:** В сети
        """)
